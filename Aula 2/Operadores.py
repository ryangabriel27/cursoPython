b = 3

# Operações:
soma = a + b
subtracao = a - b
multiplicacao = a * b
divisao = a / b
divisao_inteira = a // b
resto = a % b
potencia = a**b

print(soma, subtracao, multiplicacao, divisao, divisao_inteira, resto, potencia)

# OPERADORES RELACIONAIS -
# Numeros:
x = 5
y = 10

# Operaçoes:
igual = x == y
diferente = x != y
menor = x < y
maior = x > y
menor_igual = x <= y
maior_igual = x >= y

print(igual, diferente, menor, maior, menor_igual, maior_igual)

